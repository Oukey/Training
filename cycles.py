'''
8.7. Я уверен, что вы уже вполне созрели до олимпийского уровня!
Вот разминочная задачка с одной из олимпиад по программированию для школьников.
Винни-Пух готовится к зиме и в течении N дней решил не лакомиться медом, а относить полный горшочек объемом X горстей,
и перекладывать мёд в бочку. В первый день он донёс горшочек полным, но начиная со следующего дня он больше не смог
бороться с искушением и начал выедать из горшочка Y горстей. Причём на каждый следующий день он выедал на Y горстей
больше, чем в предыдущий день.
На входе программы три числа: N дней, X -- объём горшочка, Y -- выедаемые горсти. Посчитайте, сколько всего мёда за N
дней Винни-Пух накопит в бочке, а также определите день, начиная с которого горшочек будет пустым.
'''
day_N = int(input('Сколько дней осталось до зимы, N = '))
size_X = int(input('Сколько горстей меда помещается в горшочке, X = '))
expend_Y = int(input('Сколько горстей меда кушает Винни-Пух, Y = '))
barrel = 0
i = 0  # счетчик дней
day_Z = 0  # день когда мед в горшке закончился

for d in range(1, day_N + 1):
    if (size_X > expend_Y):
        if (d == 1):
            barrel = barrel + size_X
            i = i + 1
        else:
            barrel = barrel + size_X - expend_Y
            i = i + 1
            expend_Y = expend_Y + 1
            day_Z = i
    else:
        print('Наступила зима!')
        break
print('К зиме Винни-Пух успел накопить - ', barrel, ' горстей меда')
print('На ', day_Z, ' день Пух стал съедать весь горшочек меда по дороге к бочке')
