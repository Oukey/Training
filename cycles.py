'''
8.11. Вечная классика программирования -- факториал (в математике записывается как !).
Факториал -- это для числа N произведение всех чисел от 1 до N.
Например,
5! = 1*2*3*4*5 = 120
Напишите программу, вычисляющую факториал числа.
'''
